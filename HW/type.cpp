// Using cout, print all different kinds of data
// in each new lines (using  endl backslash n).
// comment types of each data at the end of each cout expression
// ex : cout << 3 // int

//types reference : https://www.w3schools.com/cpp/cpp_data_types.asp

#include <iostream>
using namespace std;

int main() {
  cout << 3 << endl; //integer
  cout << 2.5 << endl; //double
  cout << "Hello World!" << endl; //String
  cout << "a" << endl; //character
  cout << true << endl; //boolean
  return 0;

}
